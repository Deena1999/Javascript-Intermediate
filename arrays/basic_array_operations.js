let number=[1,2,3,4,5,6,7];

console.log(number);

// push Operation

number.push(9);
console.log(number);

// pop Operation

number.pop();
console.log(number);


//  shift Operation

number.shift();
console.log(number);



// unshift Operation

number.unshift(0);
console.log(number);



// reverse Operation

number.reverse();
console.log(number);



//   indexof Operation

const result=number.indexOf(4);
console.log(result);



//  slice Operation


console.log(number.slice(1,4));
console.log(number.slice(1,));
